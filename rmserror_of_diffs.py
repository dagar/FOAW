from pylab import *

rc('legend',fontsize=8)

def do_plot(results):
    marks = 'o^s+D'
    for i, algo in enumerate(sort(results.keys())):
        loglog(*zip(*results[algo]), marker=marks[i], label=algo)
        xlim(1000, 80000)
        ylim(0.5, 10)
        legend()

